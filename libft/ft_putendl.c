#include "libft.h"

void	ft_putendl(char const *s)
{
	int	i;

	i = 0;
	if (!s)
		return ;
	while (s[i])
	{
		ft_putchar(s[i]);
		i++;
	}
	if (s[i] == '\0')
		ft_putchar('\n');
}
